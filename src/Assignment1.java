public class Assignment1 {
	public static void main(String[] args) {
		String firstName = "Neil";
		String lastName = "Gandhi";
		char empty = ' ';
		char comma = ',';
		System.out.println(firstName + "\n" + lastName);
		System.out.println(firstName + empty + lastName);
		System.out.println(lastName + comma + empty + firstName);
		System.out.println(firstName.toUpperCase());
		System.out.println(lastName.toLowerCase());
		System.out.println(firstName.charAt(2));
		System.out.println(lastName.charAt(1));
		
	}
}
